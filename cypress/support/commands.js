Cypress.Commands.add("login", (email, senha) => {
    cy.task('loginAd', {email: email, senha: senha}).then(r =>{
        //cy.log(r)
        for (var key in r) {
            window.localStorage.setItem(key, r[key]);
        }
        cy.visit("")
        cy.wait(5000)

    })
})

